    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String texto = this.jtxtNumero.getText().replace(" ", "");
        if (texto.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Este campo debe ser rellenado");
        } else if (!texto.matches("[+-]?\\d*(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(null, "Este campo debe ser un número");
        } else {
            Integer valor = Integer.parseInt(this.jtxtNumero.getText());
            this.listaDoble.eliminarNodo(valor);
            llenarPanelLista();
            this.jtxtNumero.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

